pub enum InstructionCycle {
    NYI,
    Read,
    ReadInc,
    ReadStack,
    PopStack,
    ImmOperand,
    ZpgOperand,
    ZpgOperand2,
    ZpgIndexedOperand,
    ZpgXOperand,
    ZpgYOperand,
    AbsOperand1,
    AbsOperand2,
    AbsOperand3,
    AbsIndexedPageCross,
    AbsXOperand,
    AbsYOperand,
    IndirectXAddressLo,
    IndirectYAddressLo,
    IndirectIndexedAddressHi,
    RelOperand,
    Adc,
    Bcc,
    Bcs,
    Beq,
    Bmi,
    Bne,
    Bpl,
    Bvc,
    Bvs,
    Clc,
    Cld,
    Cli,
    Clv,
    Dey2,
    Inc2,
    Inc3,
    Inc4,
    Inx2,
    JmpAbs,
    Jsr1,
    Jsr2,
    Jsr3,
    Jsr4,
    Jsr5,
    Jsr6,
    Lda,
    Ldx,
    Ldy,
    Pha,
    Php,
    Pla,
    Plp,
    RelBranch1,
    RelBranch2,
    Rts1,
    Rts2,
    Rts3,
    Rts4,
    Rts5,
    Sec,
    Sed,
    Sei,
    Tax,
    Tay,
    Tsx,
    Txa,
    Txs,
    Tya,
}

#[derive(Debug)]
pub struct Instruction {
    pub identifier: &'static str,
    pub cycles: &'static [InstructionCycle],
}
